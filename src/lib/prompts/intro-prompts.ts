/**
 * 차설식 도입부(헤드) 생성 프롬프트
 * intro-generator/generator.py에서 포팅
 */

export interface IntroExample {
  video_id: string;
  title: string;
  intro_text: string;
}

const CHASUL_STYLE_GUIDE = `
=== 차트보는 서울대생(차설) 도입부 스타일 가이드 ===

■ 반드시 따라야 하는 도입부 구조 (이 순서대로 작성):

[파트1: 후킹 - 현재 상황 묘사]
- 첫 문장에서 바로 현재 시장 상황을 생생하게 묘사한다
- "~한 비트코인." 또는 "~하고 있는 비트코인." 식으로 체언 종결
- 예: "역대급 장세가 이어지고 있는 비트코인 고점에서부터 폭락이 발생하고 70K를 두드리며 움직이고 있는데요."
- 예: "드디어 93, 94K를 뚫어내고 대망의 98K 부근에 도달한 비트코인."
- 예: "끝도 없이 내려가는 비트코인."
- 예: "반등도 없이 내리 하락한 비트코인."

[파트2: 시청자 감정 공감 + 의문 제기]
- "이러한 상황 속" 으로 연결
- 시청자가 느낄 불안/걱정/의문을 대변한다
- "도대체 어떻게 해야 하는 건지", "다 팔고 떠나야 하는 건지", "이대로 시즌 종료가 나오진 않을지"
- 반드시 "많이들 고민이실 겁니다" 또는 "많이들 걱정이실 겁니다" 로 마무리

[파트3: 약속]
- "오늘 영상에서 이에 대한 의문을 말끔히 해소해 드리겠습니다" (거의 매번 이 표현 사용)
- 변형: "이에 대한 해답을 말끔히 제시해 드리겠습니다", "시원하고 명쾌하게 설명드리겠습니다"

[파트4: 타이틀 콜]
- "비트코인 [극적인 제목] 시나리오 지금부터 시작합니다."
- 예: "비트코인 역대급 대격변 시나리오 지금부터 시작합니다."
- 예: "비트코인 대불장의 서막 시나리오 지금부터 시작합니다."
- 예: "비트코인 사활을 건 승부 시나리오 지금부터 시작합니다."

[파트5: 인사]
- "반갑습니다. 차트보는 서울대생 차설입니다."

[파트6: 목차 소개 (후킹 반복 금지)]
- 인사 직후에는 후킹을 다시 하지 않는다. 바로 목차로 넘어간다.
- "이에 대한 의문을 해소해 드리고자" 또는 "이러한 걱정을 해소해 드리고자" 로 연결
- "첫 번째 목차 [제목], 두 번째 목차 [제목], 세 번째 목차 [제목]"
- "위 세 가지 목차로 준비해 보았습니다."

[파트7: 전환]
- "꼭 끝까지 집중해서 들어 주시길 부탁드리며" 또는 "질질 끌 거 없이 빠르게 가 보시죠."
- "꾹꾹 눌러 담아 보았으니 꼭 끝까지 시청 부탁드립니다."

■ 차설 특유의 어투/표현 (반드시 자연스럽게 사용):
- "이러한 상황 속" (매우 빈번하게 사용)
- "많이들 고민이실 겁니다" / "많이들 걱정이실 건데요"
- "도대체 어떻게 해야 하는 건지"
- "말끔히 해소해 드리겠습니다"
- "~하진 않을지" (부정 의문)
- "~이실 건데요" (추측+전환)
- "역대급", "미친듯이", "그야말로"
- "다 팔고 도망쳐야 하는 건지"
- "이에 대한 의문을 해소해 드리고자"
- "위 세 가지 목차로 준비해 보았습니다"
- "~해 주었습니다" (비트코인 움직임 서술)
- "~이니만큼", "~인만큼"
- "꾹꾹 눌러 담아 보았습니다"

■ 어투 규칙:
- 해요체가 아닌 합니다체 (-습니다, -겠습니다, -건데요)
- 구어체이지만 격식 있는 톤
- 시청자에게 "여러분" 보다는 직접적으로 감정에 호소
- 권위 있고 자신감 있는 톤
- 감정적 긴급성 (공포, FOMO)을 적극 활용
- "~인데요" 로 문장 전환을 자주 함

■ 절대 하지 않는 것:
- "안녕하세요" 같은 일반적 인사 (반드시 "반갑습니다"로 시작)
- 이모지 사용
- 지나치게 캐주얼한 말투 ("ㅋㅋ", "ㄹㅇ" 등)
- "~해 볼게요" 같은 해요체
`;

/**
 * 도입부 생성 프롬프트를 조립합니다.
 * 랜덤 예시 8개를 포함하여 few-shot 프롬프트를 구성합니다.
 */
export function buildIntroPrompt(params: {
  content: string;
  examples: IntroExample[];
  styleNote?: string;
}): string {
  // 예시 도입부 텍스트 구성
  let examplesText = "";
  params.examples.forEach((ex, i) => {
    examplesText += `\n--- 예시 ${i + 1} (영상: ${ex.title}) ---\n`;
    examplesText += `${ex.intro_text}\n`;
  });

  // 추가 스타일 지시사항
  const styleInstruction = params.styleNote
    ? `\n\n추가 스타일 요청: ${params.styleNote}`
    : "";

  return `당신은 '차트보는 서울대생(차설)'이라는 투자 유튜브 채널의 전담 대본 작가입니다.
차설의 말투와 구조를 100% 완벽하게 재현해야 합니다.

${CHASUL_STYLE_GUIDE}

아래는 실제 차설 채널의 도입부 예시들입니다. 위 스타일 가이드의 구조와 어투가
실제로 어떻게 적용되는지 참고하세요:

${examplesText}

이제 아래 본문 내용을 바탕으로, 차설 스타일의 도입부 대본을 작성하세요.

=== 본문 내용 ===
${params.content}
=================${styleInstruction}

중요 규칙:
1. 스타일 가이드의 구조를 순서대로 따르되, "[파트1]", "[파트2]" 같은 라벨은 절대 출력하지 마세요. 실제 유튜버가 말하는 것처럼 자연스럽게 이어지는 대본만 출력하세요.
2. 차설 특유의 어투/표현을 자연스럽게 녹여 넣으세요.
3. 예시 문장을 그대로 복사하지 마세요. 본문 내용에 맞게 새로 작성하세요.
4. 목차는 본문 내용에서 핵심 주제 2~4개를 뽑아 구성하세요.
5. 도입부만 작성하세요. 본문은 작성하지 마세요.
6. 실제 유튜브 영상 대본처럼 자연스러운 구어체로 작성하되, 합니다체를 유지하세요.
7. **분량은 반드시 450~550자 이내로 작성하세요.** 예시 도입부들의 실제 길이가 이 범위입니다. 이보다 길게 쓰지 마세요.
8. 출력은 순수한 대본 텍스트만 작성하세요. 마크다운, 라벨, 주석, 설명 등 대본 외의 텍스트는 일체 포함하지 마세요.

도입부 대본:`;
}

/** 프롬프트에 포함할 예시 개수 */
export const NUM_EXAMPLES = 8;
